# Iteração
# Tipo de estrutura de controle que gerencia quantas vezes um trecho de código será executado.
# Principais estruturas são: FOR, WHILE, TIMES, DO/WHILE

# TIMES executa uma repetição por um determinado número de vezes.

# Sintaxe, abre com o número de vezes de uma repetição seguido do comando (.times do), executa a condição e fecha o bloco com (end)

10.times do
    puts 'Hello!'
end